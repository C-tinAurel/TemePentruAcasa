public class CalculatorOperatiiStiintifice extends Calculator {

    public double ridicareaLaPutere(double a, double b) {
        return Math.pow(a, b);
    }

    public double radicalDintrUnNumar(double a) {
        return Math.sqrt(a);
    }

    public double logaritmDin10(double a) {
        return Math.log10(a);
    }

    public double logaritmDintrUnNumar(double a) {
        return Math.log(a);
    }
}
